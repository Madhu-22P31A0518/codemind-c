#include<stdio.h>
#include<math.h>
int main()
{
    int a,m,r,s=0,p,e,q=0;
    scanf("%d",&a);
    m=a*a;
    while(m!=0)
    {
        r=m%10;
        s=s*10+r;
        m=m/10;
    }
    e=sqrt(s);
    while(e!=0)
    {
        p=e%10;
        q=q*10+p;
        e=e/10;
    }
    if(q==a)
    {
        printf("True");
    }
    else
    {
        printf("False");
    }
}